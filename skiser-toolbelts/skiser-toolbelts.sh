# A way to manage my own repos and codebases
# - toolbelts new = make a new toolbelt
# - toolbelts add = install a toolbelt
# 
# - toolbelts {beltname} tool add = install a tool to your toolbelt (add a new repo)
# - toolbelts {beltname} tool remove = uninstall tool from toolbelt
# 
# - toolbelts update = git fetch / git status (perform belt buckle functions)
# - toolbelts upgrade = git pull (perform belt buckle functions)
# - toolbelts {beltname} buckle (perform belt buckle functions)